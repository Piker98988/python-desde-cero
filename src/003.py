"""
Interactuar con el usuario

Para interactuar con el usuario, por ejemplo, decirle cosas, usaremos la fabulosa
función `print()`. Esta función es un builtin, que quiere decir que es built-in.
Esto significa que lo uses donde lo uses va a funcionar, porque está embebida en
el propio lenguaje.
Todo lo que tenga un paréntesis al final, es una función. Todo lo que empiece por
un punto y luego tenga un paréntesis, es un método: `.upper()`.
"""

print("Hola Mundo")

"""
En este código podemos ver cómo usamos la función print. Las funciones se pueden
comparar con máquinas; máquinas en las que introducimos algo y hace algo con ese
mismo algo. Es decir, si tu tienes una función f(x), esta misma función te
devolverá resultados diferentes dependiendo de x, lo que básicamente se puede
comparar con una máquina. Digamos que:

$$ f(x) = x + 1 $$

Entonces, esto querría decir que si nosotros ponemos 1 en la máquina f(x), saldrá 2:

$$ f(1) = 1 + 1 = 2 $$

En caso de print(), nosotros introducimos una cadena, la cual será manipulada y
se mandará a stdout, es decir, la salida de texto del programa.
"""

cajón: str = "mi variable cajón"

